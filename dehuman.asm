Start
    SEI ; disable interrupts
    CLD ; clear BCD math bit    
    LDX #$FF ; x = 255
    TXS ; use x to reset stack pointer
    LDA #0 ; store 0 in a, will use this to clear mem           
ClearMem 
    STA 0,X     ; put value of a (0) into the mem loc x+0
    DEX ; decrement x   
    BNE ClearMem ; keep looping til x hits 0
	
; Other pre-drawing setup
	LDA #7
	STA ColorChangePointer
	LDA #6
	STA ColorChangeCounter
	LDA #29
	STA SpeedChangePointer
	LDA #10
	STA SpeedChangeCounter
	LDA #191
	STA MasterScanlineCounter
	
; Setup sliding range initial values

	LDA #185
	STA RangeUpperMax
	LDA #5
	STA RangeLowerMin
	LDA #3
	STA RangeSizeMin
	LDA #100
	STA RangeSizeMax

	LDA #125
	STA RangeUpperBound
	LDA #75
	STA RangeLowerBound
	LDA #1
	STA RangeUpperBoundDir
	LDA #0
	STA RangeLowerBoundDir
	LDA #1
	STA RangeUpperBoundSpeed
	LDA #1
	STA RangeLowerBoundSpeed
	
	; Initialize random seeds
	
	LDA #77
	STA rand1
	LDA #202
	STA rand2


    
; Let's draw a frame    
MainLoop
    LDA  #2 ; a = 2, need bit 1 for enabling VSYNC
    STA  VSYNC  ; enable VSYNC
    STA  WSYNC  ; hold VSYNC for 3 scanlines by WSYNC'ing 3x
    STA  WSYNC  ; WSYNC doesn't care what value is passed
    STA  WSYNC  
    LDA  #43 ; a = 43. 2798 cycles to wait. Using 64 cycle timer, that's about 43       
    STA  TIM64T ; store that in the timer
    LDA #0      ; a = 0, need 0 for disabling VSYNC     
    STA  VSYNC  ; disable vsync

; SAFE ZONE FOR PRE-VISIBLE FRAME STUFF


	
	DEC ColorChangeCounter
	BNE EndColorChangeBlock
	LDX ColorChangePointer
	LDA RedColors-1,X
	STA FgColOuter
	LDA RedColors,X
	STA BgColOuter
	LDA RedColors+2,X
	STA Player0ColOuter
	LDA RedColors+3,X
	STA Player1ColOuter
	LDA GrayCycle-1,X
	STA FgColInner
	LDA GrayCycle,X
	STA BgColInner
	LDA GrayCycle+2,X
	STA Player0ColInner
	LDA GrayCycle+3,X
	STA Player1ColInner
	LDA #20
	STA ColorChangeCounter
	DEC ColorChangePointer
	BNE EndColorChangeBlock
	LDA #7
	STA ColorChangePointer
EndColorChangeBlock    


	DEC SpeedChangeCounter
	BNE EndSpeedChangeBlock
	LDX SpeedChangePointer
	LDA Player0SpeedRange-1,X
	STA Player0Speed
	LDA Player1SpeedRange,X
	STA Player1Speed
	LDA #10
	STA SpeedChangeCounter
	DEC SpeedChangePointer
	BNE EndSpeedChangeBlock
	LDA #29
	STA SpeedChangePointer

EndSpeedChangeBlock
    

;	Apply range changes
	
	LDA RangeUpperBoundDir
	CMP #1
	BEQ RangeUpperBoundAdd
	CMP #0
	BEQ RangeUpperBoundSub
RangeUpperBoundAdd
	LDA RangeUpperBound
	ADC RangeUpperBoundSpeed
	STA RangeUpperBound
	JMP RangeUpperBoundSpeedEnd
RangeUpperBoundSub
	LDA RangeUpperBound
	SBC RangeUpperBoundSpeed
	STA RangeUpperBound
RangeUpperBoundSpeedEnd
	CMP RangeUpperMax
	BCC NoUpperBoundMaxReset
	LDA RangeUpperMax
	STA RangeUpperBound
	LDA #0
	STA RangeUpperBoundDir
	LDA RangeUpperBoundSpeed
	CMP #1
	BEQ IncreaseUpperBoundSpeed
	LDA #1
	STA RangeUpperBoundSpeed
	JMP NoUpperBoundMaxReset
NoUpperBoundMaxReset
	LDA RangeLowerBound
	ADC RangeSizeMin
	CMP RangeUpperBound
	BCC NoUpperBoundMinReset
	STA RangeUpperBound
	LDA #1
	STA RangeUpperBoundDir
	LDA RangeUpperBoundSpeed
	CMP #1
	BEQ IncreaseUpperBoundSpeed
	LDA #1
	STA RangeUpperBoundSpeed
	JMP NoUpperBoundMinReset
IncreaseUpperBoundSpeed
	LDA #3
	STA RangeUpperBoundSpeed
NoUpperBoundMinReset


	LDA RangeLowerBoundDir
	CMP #1
	BEQ RangeLowerBoundAdd
	CMP #0
	BEQ RangeLowerBoundSub
RangeLowerBoundAdd
	LDA RangeLowerBound
	ADC RangeLowerBoundSpeed
	STA RangeLowerBound
	JMP RangeLowerBoundSpeedEnd
RangeLowerBoundSub
	LDA RangeLowerBound
	SBC RangeLowerBoundSpeed
	STA RangeLowerBound
RangeLowerBoundSpeedEnd
	CMP RangeLowerMin
	BCS NoLowerBoundMinReset
	LDA RangeLowerMin
	STA RangeLowerBound
	LDA #1
	STA RangeLowerBoundDir
	LDA RangeLowerBoundSpeed
	CMP #1
	BEQ IncreaseLowerBoundSpeed
	LDA #1
	STA RangeLowerBoundSpeed
NoLowerBoundMinReset
	LDA RangeUpperBound
	SBC RangeSizeMin
	CMP RangeLowerBound
	BCS NoLowerBoundMaxReset
	STA RangeLowerBound
	LDA #0
	STA RangeLowerBoundDir
	LDA RangeLowerBoundSpeed
	CMP #1
	BEQ IncreaseLowerBoundSpeed
	LDA #1
	STA RangeLowerBoundSpeed
	JMP NoLowerBoundMaxReset
IncreaseLowerBoundSpeed
	LDA #2
	STA RangeLowerBoundSpeed
NoLowerBoundMaxReset


	; Compute ranges

	LDA #191
	SBC RangeUpperBound
	STA RangeASize
	LDA RangeUpperBound
	SBC RangeLowerBound
	STA RangeBSize
	LDA RangeLowerBound
	STA RangeCSize
	
	
	; Init iterator registers
	
	LDY RangeASize
	LDX #191
	
	; Set colors for first region
	
	LDA FgColOuter
	STA COLUPF
	LDA BgColOuter
	STA COLUBK
	LDA Player0ColOuter
	STA COLUP0
	LDA Player1ColOuter
	STA COLUP1
	
	JSR randomize
	STA GRP0
	JSR randomize
	STA GRP1
	
	; Set player speeds
	
	LDA Player0Speed
	STA HMP0
	LDA Player1Speed
	STA HMP1
	
	; Set some randoms we'll use per frame
	
	JSR randomize
	STA RandSpriteA
	JSR randomize
	LSR A
	LSR A
	STA RandSpriteB
	JSR randomize
	LSR A
	LSR A
	LSR A
	STA RandSpriteC
	JSR randomize
	STA RandSpriteD
	JSR randomize
	ROL A
	ROL A
	STA RandSpriteE
	JSR randomize
	ROL A
	ROL A
	ROL A
	ROL A
	STA RandSpriteF

	
; END SAFE ZONE FOR PRE-VISIBLE FRAME STUFF

WaitForVblankEnd
    LDA INTIM     
    BNE WaitForVblankEnd 
    STA WSYNC 
    STA VBLANK ; needs to be 0, we get that from timer end
    STA HMOVE
	STA WSYNC

ScanLoopA
    STA WSYNC
    
    LDA PF0SpriteA-1,X
    STA PF0 
        
    LDA PF1SpriteA-1,X
    STA PF1
	
    
    LDA PF2SpriteA-1,X
    STA PF2 
   
 
    LDA PF0SpriteB-1,X
    STA PF0 
    
    LDA PF1SpriteB-1,X
    STA PF1
	
	NOP
	NOP
	NOP
    
    LDA PF2SpriteB-1,X
    STA PF2
	
    
    
EndLineA 

	DEX
    DEY
    BNE ScanLoopA
	STA WSYNC
	LDY RangeBSize
	LDA FgColInner
	STA COLUPF
	LDA BgColInner
	STA COLUBK
	LDA Player0ColInner
	STA COLUP0
	LDA Player1ColInner
	STA COLUP1
	JMP ScanLoopBWsyncBypass

ScanLoopB
    STA WSYNC
ScanLoopBWsyncBypass

    LDA AltPF0SpriteA-1,X
	ORA RandSpriteA
    STA PF0 
        
    LDA AltPF1SpriteA-1,X
	ORA RandSpriteB
    STA PF1
	
    
    LDA AltPF2SpriteA-1,X
	ORA RandSpriteC
    STA PF2 
   	
 
    LDA AltPF0SpriteB-1,X
	ORA RandSpriteD
    STA PF0 
    
    LDA AltPF1SpriteB-1,X
	ORA RandSpriteE
    STA PF1
	
    
    LDA AltPF2SpriteB-1,X
	ORA RandSpriteF
    STA PF2
    
    
EndLineB 

	DEX
    DEY
    BNE ScanLoopB
	STA WSYNC
	LDY RangeCSize
	LDA FgColOuter
	STA COLUPF
	LDA BgColOuter
	STA COLUBK
	LDA Player0ColOuter
	STA COLUP0
	LDA Player1ColOuter
	STA COLUP1
	JMP ScanLoopCWsyncBypass

ScanLoopC
    STA WSYNC
ScanLoopCWsyncBypass
 
    LDA PF0SpriteA-1,X
    STA PF0 
        
    LDA PF1SpriteA-1,X
    STA PF1
	
    
    LDA PF2SpriteA-1,X
    STA PF2 
   
 
    LDA PF0SpriteB-1,X
    STA PF0 
    
	NOP
	
    LDA PF1SpriteB-1,X
    STA PF1
	
	NOP
	NOP
	
	
    
    LDA PF2SpriteB-1,X
    STA PF2
	
	
    
    
EndLineC

	DEX
    DEY
    BNE ScanLoopC ; keep going til we run out of scanlines  	
	
    LDA #2      ; a = 2, will use that for VBLANK (make output invisible for overscan)  
    STA WSYNC     	
    STA VBLANK  ; disable output

; OVERSCAN START
	
	LDA #33 ; 2,112 CPU cycles, what will we ever do with the time???
	STA TIM64T	

; OVERSCAN END
OverScanWait
	LDA INTIM
    BNE OverScanWait
	STA WSYNC
    JMP  MainLoop
	
randomize
	lda rand1
	asl
	ror rand2
	bcc noEor1
	eor #$DB
noEor1:
	asl
	ror rand2
	bcc noEor2
	eor #$DB
noEor2:
	sta rand1
	eor rand2
	rts

