	STA AUDC0
	LDA RandomValHolder1
	ROL
	ROL
	ROL
	STA AUDF0
EndChan0
	DEC DrumDelayCounter
	BNE NoResetDrumDelayCounter
	LDA #96
	STA DrumDelayCounter
NoResetDrumDelayCounter

	DEC NoteDelayCounter
	BEQ ChangeNote
	JMP DontChangeNote
ChangeNote
	LDA RangeUpperBound
	;ROL
	;ROL
	;ROL
	STA AUDF1
	LDA #6
	STA AUDC1
	LDA #3
	STA NoteDelayCounter
	JMP DoneAudio
DontChangeNote
	LDA RangeLowerBound
	ROL
	ROL
	STA AUDF1
    LDA #6
	STA AUDC1

DoneAudio

; OVERSCAN END
OverScanWait
	LDA INTIM
    BNE OverScanWait
	STA WSYNC
    JMP  MainLoop
	
randomize
	lda rand1
	asl
	ror rand2
	bcc noEor1
	eor #$DB
noEor1:
	asl
	ror rand2
	bcc noEor2
	eor #$DB
noEor2:
	sta rand1
	eor rand2
	rts

