NoColorChange
	LDA #$36
	STA COLUPF
	LDA #$30
	STA COLUBK
	LDA #$12
	STA COLUP0
	
	LDA #$22
	STA COLUP1
    
    
    LDA #0 
    STA PF0
    STA PF1
    STA PF2   

	LDA #$90
	STA HMP0
	LDA #$30
	STA HMP1
	
	
;	Apply range changes
	
	LDA RangeUpperBoundDir
	CMP #1
	BEQ RangeUpperBoundAdd
	CMP #0
	BEQ RangeUpperBoundSub
RangeUpperBoundAdd
	LDA RangeUpperBound
	ADC RangeUpperBoundSpeed
	STA RangeUpperBound
	JMP RangeUpperBoundSpeedEnd
RangeUpperBoundSub
	LDA RangeUpperBound
	SBC RangeUpperBoundSpeed
	STA RangeUpperBound
RangeUpperBoundSpeedEnd
	CMP RangeUpperMax
	BCC NoUpperBoundMaxReset
	LDA RangeUpperMax
	STA RangeUpperBound
	LDA #0
	STA RangeUpperBoundDir
NoUpperBoundMaxReset
	LDA RangeLowerBound
	ADC RangeSizeMin
	CMP RangeUpperBound
	BCC NoUpperBoundMinReset
	STA RangeUpperBound
	LDA #1
	STA RangeUpperBoundDir
NoUpperBoundMinReset


	LDA RangeLowerBoundDir
	CMP #1
	BEQ RangeLowerBoundAdd
	CMP #0
	BEQ RangeLowerBoundSub
RangeLowerBoundAdd
	LDA RangeLowerBound
	ADC RangeLowerBoundSpeed
	STA RangeLowerBound
	JMP RangeLowerBoundSpeedEnd
RangeLowerBoundSub
	LDA RangeLowerBound
	SBC RangeLowerBoundSpeed
	STA RangeLowerBound
RangeLowerBoundSpeedEnd
	CMP RangeLowerMin
	BCS NoLowerBoundMinReset
	LDA RangeLowerMin
	STA RangeLowerBound
	LDA #1
	STA RangeLowerBoundDir
NoLowerBoundMinReset
	LDA RangeUpperBound
	SBC RangeSizeMin
	CMP RangeLowerBound
	BCS NoLowerBoundMaxReset
	STA RangeLowerBound
	LDA #0
	STA RangeLowerBoundDir
NoLowerBoundMaxReset


	; Compute ranges

	LDA #191
	SBC RangeUpperBound
	STA RangeASize
	LDA RangeUpperBound
	SBC RangeLowerBound
	STA RangeBSize
	LDA RangeLowerBound
	STA RangeCSize
	
	
	; Init iterator registers
	
	LDY RangeASize
	LDX #191


WaitForVblankEnd
    LDA INTIM     
    BNE WaitForVblankEnd 
    STA WSYNC 
    STA VBLANK ; needs to be 0, we get that from timer end
    STA HMOVE
	STA WSYNC

ScanLoopA
    STA WSYNC
    
    LDA PF0SpriteA-1,X
    STA PF0 
        
    LDA PF1SpriteA-1,X
    STA PF1
	
    
    LDA PF2SpriteA-1,X
    STA PF2 
   
	STA GRP0
 
    LDA PF0SpriteB-1,X
    STA PF0 
    
    LDA PF1SpriteB-1,X
    STA PF1
    
    LDA PF2SpriteB-1,X
    STA PF2
	
	STA GRP1
    
    
EndLineA 

	DEX
    DEY
    BNE ScanLoopA
	STA WSYNC
	LDY RangeBSize
	LDA #$96
	STA COLUPF
	LDA #$82
	STA COLUBK
	JMP ScanLoopBWsyncBypass

ScanLoopB
    STA WSYNC
ScanLoopBWsyncBypass

    LDA AltPF0SpriteA-1,X
    STA PF0 
        
    LDA AltPF1SpriteA-1,X
    STA PF1
	
    
    LDA AltPF2SpriteA-1,X
    STA PF2 
   
	STA GRP0
 
    LDA AltPF0SpriteB-1,X
    STA PF0 
    
    LDA AltPF1SpriteB-1,X
    STA PF1
	
	NOP
	NOP
    
    LDA AltPF2SpriteB-1,X
    STA PF2
	
	STA GRP1
	
	;LDA RedColors-1,X
	;LDA #$3A
	;STA COLUP0
	;LDA #$24
	;STA COLUP1
    
    
EndLineB 

	DEX
    DEY
    BNE ScanLoopB
	STA WSYNC
	LDY RangeCSize
	LDA #$36
	STA COLUPF
	LDA #$30
	STA COLUBK
	JMP ScanLoopCWsyncBypass

ScanLoopC
    STA WSYNC
ScanLoopCWsyncBypass
 
    LDA PF0SpriteA-1,X
    STA PF0 
        
    LDA PF1SpriteA-1,X
    STA PF1
	
    
    LDA PF2SpriteA-1,X
    STA PF2 
   
	STA GRP0
 
    LDA PF0SpriteB-1,X
    STA PF0 
    
    LDA PF1SpriteB-1,X
    STA PF1
	
	NOP
	NOP
    
    LDA PF2SpriteB-1,X
    STA PF2
	
	STA GRP1
	
	LDA RedColors-1,X
	STA COLUP0
	STA COLUP1
    
    
EndLineC

	DEX
    DEY
    BNE ScanLoopC ; keep going til we run out of scanlines  	
	
    LDA #2      ; a = 2, will use that for VBLANK (make output invisible for overscan)  
    STA WSYNC     	
    STA VBLANK  ; disable output
    LDY #30      ; x = 30, 30 lines of overscan

	DEC SpecialReg1
	LDA SpecialReg1
	BNE DontResetSpecial
	LDA #$FF
	STA SpecialReg1
DontResetSpecial


	

