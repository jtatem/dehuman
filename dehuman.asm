	STA RangeLowerBoundSpeed
DoneLowerBound


	; Compute ranges

	LDA #191
	SBC RangeUpperBound
	STA RangeASize
	LDA RangeUpperBound
	SBC RangeLowerBound
	STA RangeBSize
	LDA RangeLowerBound
	STA RangeCSize
	
	
	; Init iterator registers
	
	LDY RangeASize
	LDX #191
	
	; Set colors for first region
	
	LDA FgColOuter
	STA COLUPF
	LDA BgColOuter
	STA COLUBK
	LDA Player0ColOuter
	STA COLUP0
	LDA Player1ColOuter
	STA COLUP1
	
	JSR randomize
	STA GRP0
	JSR randomize
	STA GRP1
	
	; Set player speeds
	
	LDA Player0Speed
	STA HMP0
	LDA Player1Speed
	STA HMP1
	
	; Set some randoms we'll use per frame
	
	JSR randomize
	STA RandSpriteA
	JSR randomize
	STA RandSpriteB
	JSR randomize
	STA RandSpriteC
	JSR randomize
	STA RandSpriteD
	JSR randomize
	STA RandSpriteE
	JSR randomize
	STA RandSpriteF

	
; END SAFE ZONE FOR PRE-VISIBLE FRAME STUFF

WaitForVblankEnd
    LDA INTIM     
    BNE WaitForVblankEnd 
    STA WSYNC 
    STA VBLANK ; needs to be 0, we get that from timer end
    STA HMOVE
	STA WSYNC

ScanLoopA
    STA WSYNC
    
    LDA PF0SpriteA-1,X
    STA PF0 
        
    LDA PF1SpriteA-1,X
    STA PF1
	
    
    LDA PF2SpriteA-1,X
    STA PF2 
   
 
    LDA PF0SpriteB-1,X
    STA PF0 
    
