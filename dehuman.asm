    processor 6502
    include vcs.h
    org $F000
	
SpecialReg1 = $80
	
Start
    SEI ; disable interrupts
    CLD ; clear BCD math bit    
    LDX #$FF ; x = 255
    TXS ; use x to reset stack pointer
    LDA #0 ; store 0 in a, will use this to clear mem           
ClearMem 
    STA 0,X     ; put value of a (0) into the mem loc x+0
    DEX ; decrement x   
    BNE ClearMem ; keep looping til x hits 0
    
; Let's draw a frame    
MainLoop
    LDA  #2 ; a = 2, need bit 1 for enabling VSYNC
    STA  VSYNC  ; enable VSYNC
    STA  WSYNC  ; hold VSYNC for 3 scanlines by WSYNC'ing 3x
    STA  WSYNC  ; WSYNC doesn't care what value is passed
    STA  WSYNC  
    LDA  #43 ; a = 43. 2798 cycles to wait. Using 64 cycle timer, that's about 43       
    STA  TIM64T ; store that in the timer
    LDA #0      ; a = 0, need 0 for disabling VSYNC     
    STA  VSYNC  ; disable vsync

; Can use the cycles here for game logic, waiting for the visible
; portion of the display
   
    LDA #0
    STA CTRLPF 
    
    LDA #$30
    STA COLUPF
	
	LDA #$32
	STA COLUP0
	
	LDA #$34
	STA COLUP1
    
    
    LDA #0 
    STA PF0
    STA PF1
    STA PF2   

	LDA #$80
	STA HMP0
	LDA #$60
	STA HMP1

WaitForVblankEnd
    LDA INTIM ; check to see if timer has run out       
    BNE WaitForVblankEnd ; if it hasn't, keep waiting   
    
    
    ;LDY #191 ; y = 191, allows y to be an index of which scanline we're on
    LDY #191
    STA WSYNC ; WSYNC prevents VBLANK from turning on image mid-line
    STA VBLANK ; a = 0 from timer running out   
    STA HMOVE
	STA WSYNC

ScanLoop
    STA WSYNC
    
    LDA PF0SpriteA-1,Y
    STA PF0 
        
    LDA PF1SpriteA-1,Y
    STA PF1
	
    
    LDA PF2SpriteA-1,Y 
    STA PF2 
   
	STY SpecialReg1
	EOR SpecialReg1
	STA GRP0

 
    LDA PF0SpriteB-1,Y
    STA PF0 
    
    LDA PF1SpriteB-1,Y
    STA PF1
    
    LDA PF2SpriteB-1,Y
    STA PF2
	
	AND SpecialReg1
	STA GRP1
    
    
EndLine 

    DEY
    BNE ScanLoop ; keep going til we run out of scanlines       

    LDA #2      ; a = 2, will use that for VBLANK (make output invisible for overscan)  
    STA WSYNC  ; finish prev line       
    STA VBLANK  ; disable output
    LDY #30      ; x = 30, 30 lines of overscan
OverScanWait
; More game logic can go here 
    STA WSYNC ; finish prev line
    DEY ; decrement scanline index
    BNE OverScanWait ; keep going til we run out of scanlines
    JMP  MainLoop  ; start over for a new frame 
