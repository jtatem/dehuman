	CPY #46
	BCS PlaySnareDrum
	CPY #36
	BCS PlayDefaultSound
	CPY #33
	BCS PlayKickDrum
	CPY #24
	BCS PlayDefaultSound
	CPY #21
	BCS PlayKickDrum
	CPY #12
	BCS PlayDefaultSound
	CPY #10
	BCS PlaySnareDrum
	CPY #0
	BCS PlayDefaultSound
	JMP PlayDefaultSound
LowerBoundFucker
	LDA RangeLowerBound
	SBC #10
	STA RangeLowerBound
	JMP EndChan0
UpperBoundFucker
	LDA RangeUpperBound
	ADC #5
	STA RangeUpperBound
	JMP PlaySnareDrum		
