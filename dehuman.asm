    LDA PF1SpriteB-1,X
    STA PF1
	
	NOP
	NOP
    
    LDA PF2SpriteB-1,X
    STA PF2
	
	LDA INPT4
	BMI EndLineA
	NOP
	NOP
	NOP
	NOP
	NOP
	NOP
    
EndLineA

	DEX
    DEY
    BNE ScanLoopA
	STA WSYNC
	LDY RangeBSize
	LDA FgColInner
	STA COLUPF
	LDA BgColInner
	STA COLUBK
	LDA Player0ColInner
	STA COLUP0
	LDA Player1ColInner
	STA COLUP1
	JMP ScanLoopBWsyncBypass

ScanLoopB
    STA WSYNC
ScanLoopBWsyncBypass

    LDA AltPF0SpriteA-1,X
	ORA RandSpriteA
    STA PF0 
        
    LDA AltPF1SpriteA-1,X
	ORA RandSpriteB
    STA PF1
	
    
    LDA AltPF2SpriteA-1,X
	ORA RandSpriteC
    STA PF2 
   	
 
    LDA AltPF0SpriteB-1,X
	ORA RandSpriteD
    STA PF0 
    
    LDA AltPF1SpriteB-1,X
	ORA RandSpriteE
    STA PF1
	
    
    LDA AltPF2SpriteB-1,X
	ORA RandSpriteF
    STA PF2
    
    
EndLineB 

	DEX
    DEY
    BNE ScanLoopB
	STA WSYNC
	LDY RangeCSize
	LDA FgColOuter
	STA COLUPF
	LDA BgColOuter
	STA COLUBK
	LDA Player0ColOuter
	STA COLUP0
	LDA Player1ColOuter
	STA COLUP1
	JMP ScanLoopCWsyncBypass

ScanLoopC
    STA WSYNC
ScanLoopCWsyncBypass
 
    LDA PF0SpriteA-1,X
    STA PF0 
        
    LDA PF1SpriteA-1,X
    STA PF1
	
    
    LDA PF2SpriteA-1,X
    STA PF2 
   
 
    LDA PF0SpriteB-1,X
    STA PF0 
    
	NOP
	
    LDA PF1SpriteB-1,X
    STA PF1
	
	NOP
	NOP
	
	
    
    LDA PF2SpriteB-1,X
    STA PF2
	
	
    
    
EndLineC

	DEX
    DEY
    BNE ScanLoopC ; keep going til we run out of scanlines  	
	
    LDA #2      ; a = 2, will use that for VBLANK (make output invisible for overscan)  
    STA WSYNC     	
    STA VBLANK  ; disable output

; OVERSCAN START
	
	LDA #33 ; 2,112 CPU cycles, what will we ever do with the time???
	STA TIM64T	
	
	; Music shit
	
	JSR randomize
	STA RandomValHolder1

	LDA INPT4
	BPL PlayKickDrum
	LDA INPT5
	BPL PlaySnareDrum
	LDA #%00100000
	BIT SWCHA
	BEQ LowerBoundFucker
	LDA #%00010000
	BIT SWCHA
	BEQ UpperBoundFucker
	LDY DrumDelayCounter
	CPY #94
	BCS PlayKickDrum
	CPY #84
	BCS PlayDefaultSound
	CPY #82
	BCS PlaySnareDrum
	CPY #72
	BCS PlayDefaultSound
	CPY #69
