RangeLowerBoundSpeed = $95
MasterScanlineCounter = $96


RangeASize = $97
RangeBSize = $98
RangeCSize = $99

RangeUpperMax = $A0
RangeLowerMin = $A1
RangeSizeMin = $A2
RangeSizeMax = $A3

BgColOuter = $A4
FgColOuter = $A5
BgColInner = $A6
FgColInner = $A7

SpeedChangeCounter = $A8
SpeedChangePointer = $A9
Player0Speed = $AA
Player1Speed = $AB

ColorChangeCounter = $B0
ColorChangePointer = $B1
Player0ColOuter = $B2
Player1ColOuter = $B3
Player0ColInner = $B4
Player1ColInner = $B5

RandSpriteA = $B6
RandSpriteB = $B7
RandSpriteC = $B8
RandSpriteD = $B9
RandSpriteE = $BA
